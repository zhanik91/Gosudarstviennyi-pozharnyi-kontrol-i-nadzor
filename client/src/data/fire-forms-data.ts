export type FormRow = {
  id: string;
  number: string;
  label: string;
  children?: FormRow[];
};

export type FlattenedFormRow = {
  id: string;
  number: string;
  label: string;
  depth: number;
};

export const flattenFormRows = (rows: FormRow[], depth = 0): FlattenedFormRow[] =>
  rows.flatMap((row) => {
    const current: FlattenedFormRow = {
      id: row.id,
      number: row.number,
      label: row.label,
      depth,
    };
    if (!row.children?.length) {
      return [current];
    }
    return [current, ...flattenFormRows(row.children, depth + 1)];
  });

