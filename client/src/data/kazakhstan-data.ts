export function getRegionByCode(code: string) {
  return REGIONS.find(region => region.code === code);
}

export function getDistrictsByRegion(regionCode: string) {
  return DISTRICTS[regionCode] || [];
}

export function getCitiesByRegion(regionCode: string) {
  return MAJOR_CITIES.filter(city => city.region === regionCode);
}

export function getCauseByCode(code: string) {
  return FIRE_CAUSES.find(cause => cause.code === code);
}

export function getObjectTypeByCode(code: string) {
  return OBJECT_TYPES.find(type => type.code === code);
}

export function getDepartmentsByRegion(regionCode: string) {
  return FIRE_DEPARTMENTS.filter(dept => dept.region === regionCode);
}
